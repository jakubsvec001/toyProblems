import powerset from './powerset';
import { tests, deepSort } from './tests';

describe('deepSort', () => {
  it('1', () => {
    expect(
      deepSort([[2, 3], [], [2], [1, 2, 3], [1, 2], [1, 3], [3], [1]]),
    ).toEqual([[], [1], [2], [3], [1, 2], [1, 3], [2, 3], [1, 2, 3]]);
  });
});


describe('powerset', () => {
  it('1', () => {
    expect(deepSort(powerset([1, 2, 3]))).toEqual(
      deepSort([[], [1], [2], [3], [1, 2], [1, 3], [2, 3], [1, 2, 3]]),
    );
  });
  it('2', () => {
    expect(powerset([1, 2])).toEqual([[], [1], [2], [1, 2]]);
  });
  it('3', () => {
    expect(deepSort(powerset([1, 2, 3, 4]))).toEqual(
      deepSort([
        [],
        [1],
        [2],
        [1, 2],
        [3],
        [1, 3],
        [2, 3],
        [1, 2, 3],
        [4],
        [1, 4],
        [2, 4],
        [1, 2, 4],
        [3, 4],
        [1, 3, 4],
        [2, 3, 4],
        [1, 2, 3, 4],
      ]),
    );
  });
  it('4', () => {
    expect(deepSort(powerset([1, 2, 3, 4, 5]))).toEqual(
      deepSort([
        [],
        [1],
        [2],
        [1, 2],
        [3],
        [1, 3],
        [2, 3],
        [1, 2, 3],
        [4],
        [1, 4],
        [2, 4],
        [1, 2, 4],
        [3, 4],
        [1, 3, 4],
        [2, 3, 4],
        [1, 2, 3, 4],
        [5],
        [1, 5],
        [2, 5],
        [1, 2, 5],
        [3, 5],
        [1, 3, 5],
        [2, 3, 5],
        [1, 2, 3, 5],
        [4, 5],
        [1, 4, 5],
        [2, 4, 5],
        [1, 2, 4, 5],
        [3, 4, 5],
        [1, 3, 4, 5],
        [2, 3, 4, 5],
        [1, 2, 3, 4, 5],
      ]),
    );
  });
});
